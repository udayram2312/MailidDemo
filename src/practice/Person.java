package practice;

public class Person {
    private String name;
    private int age;


    public Person(){

    }
    public Person(String name,int age){
        this.name = name;
        this.age = age;
    }
    public String getName(){
        return name;
    }
    public int getAge(){
        return age;
    }
    public String setName(){
        return name;
    }
    public int setAge(){
        return age;
    }
}
